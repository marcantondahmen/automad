use Automad\Core\Automad;
use Automad\Engine\Processors\TemplateProcessor;

/**
 * A global helper function that can be used to load template files from within
 * a native PHP function inside a template file.
 *
 * @param string $file
 * @param Automad $Automad
 * @return string
 */
function inc(string $file, Automad $Automad): string {
	$TemplateProcessor = TemplateProcessor::create($Automad);
	$template = $Automad->loadTemplate($file);

	return $TemplateProcessor->process($template, dirname($file));
}
