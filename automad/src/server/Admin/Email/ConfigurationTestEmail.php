namespace Automad\Admin\Email;

use Automad\Admin\Email\Components\Body;
use Automad\Admin\Email\Components\Heading;
use Automad\Admin\Email\Components\Paragraph;
use Automad\Core\Text;

defined('AUTOMAD') or die('Direct access not permitted!');

/**
 * A configuration test template.
 *
 * @author Marc Anton Dahmen
 * @copyright Copyright (c) 2026 by Marc Anton Dahmen - https://marcdahmen.de
 * @license See LICENSE.md for license information
 */
class ConfigurationTestEmail {
	/**
	 * Render a test email body.
	 *
	 * @return string The rendered email body
	 */
	public static function render(): string {
		return Body::render(
			array(
				Heading::render(Text::get('emailTestSuccessHeading')),
				Paragraph::render(Text::get('emailTestSuccessText'))
			)
		);
	}
}
