/**
 * Transition dom changes when supported.
 *
 * @param update
 */
export const transition = (update: () => void): void => {
	if (
		document.startViewTransition &&
		!window.matchMedia('(prefers-reduced-motion: reduce)').matches
	) {
		document.startViewTransition(update);
	} else {
		update();
	}
};
